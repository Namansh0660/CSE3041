str = 'python for Data Science'
print(len(str))                       # 23
print(str.upper())                    # PYTHON FOR DATA SCIENCE
print(str.lower())                    # python for data science
print(str.replace('for',4))           # python 4 Data Science
print(str.strip())                    # python for Data Science
print(str.capitalize())               # Python for Data Science
print(str.islower())                  # false
print(str.find('for'))                # 7
print(str.count('a'))                 # 2
print(str.isspace())                  # false
print(str.isdigit())                  # false
